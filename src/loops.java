import java.util.*;

public class loops {
    public static void main(String[] args) {
        int i = 1;
        System.out.println("For loop output: ");
        for( ;i <= 10; ){
            System.out.println(i);
            i++;
        }

        int j = 1;
        System.out.println(" ");
        System.out.println("While loop output: ");
        while(j <= 10)
        {
            System.out.println(j);
            j++;
        }
    }
}
